    private void podatkiMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_podatkiMouseEntered
        // TODO add your handling code here:
        int i = ime_vnos.getText().length();
        int p = priimek_vnos.getText().length();
        int n = naslov_vnos.getText().length();
        int k = kraj_vnos.getText().length();
        int pos = postna_st_vnos.getText().length();
        int e = mail_vnos.getText().length();
        int t = telefon_vnos.getText().length();
        
        String temp = status.getText();
        Color tmp = status.getForeground();
        
        if (i==0 || p==0 || n==0 || k==0 || pos==0 || e==0 || t==0) {
            status.setForeground(Color.red);
            status.setText("Status: Izpolnite vse osebne podatke.");
        }
        else {
            int posta = postna_st_vnos.getText().length();
            if (posta != 4) {
                status.setForeground(Color.red);
                status.setText("Status: Vpisana poštna številka ni veljavna.");
            }
            for (int in=0; in<posta; in++) {
                if (!postna_st_vnos.getText().matches("[0-9]+")) {
                    status.setForeground(Color.red);
                    status.setText("Status: Vpisana poštna številka ni veljavna.");
                    break;
                }
            }
            
            boolean alfa = false;
            int mail = mail_vnos.getText().length();
            for (int j=0; j<mail; j++) {
                if (mail_vnos.getText().charAt(j) == '@') {
                    alfa = true;
                    break;
                }
            }
            if (!alfa) {
                status.setForeground(Color.red);
                status.setText("Status: Vpisan e-mail naslov ni pravilen.");
            }
        }
    }//GEN-LAST:event_podatkiMouseEntered

