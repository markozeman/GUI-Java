    private void formMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseEntered
        // TODO add your handling code here:
        boolean izpolnjeno = false;
        
        if ((majhen.isSelected() || srednji.isSelected() || velik.isSelected()) && (rocni.isSelected() || avtomatski.isSelected()) 
               && (bencinski.isSelected() || dizelski.isSelected())) {
            izpolnjeno = true;
        }
        
        if (!izpolnjeno) {
            status.setForeground(Color.black);
            status.setText("Status: Izpolnite podatke.");
        }
        else {
            int dP = dan_prevzema.getSelectedIndex() +1;
            int mP = mesec_prevzema.getSelectedIndex() +1;
            int lP = leto_prevzema.getSelectedIndex() +2015;
            int dV = dan_vrnitve.getSelectedIndex() +1;
            int mV = mesec_vrnitve.getSelectedIndex() +1;
            int lV = leto_vrnitve.getSelectedIndex() +2015;
            int uP = (int) ura_prevzema_vnos.getValue();
            int uV = (int) ura_vrnitve_vnos.getValue();
            
            boolean pravilno = true;

            if ((lV < lP) || (lV == lP && mV < mP) || (lV == lP && mV == mP && dV < dP) || (lV == lP && mV == mP && dV == dP && uV <= uP)) {
                status.setForeground(Color.red);
                status.setText("Status: Datum vrnitve je pred datumom prevzema.");
                pravilno = false;
                cena_na_dan_izpis.setText("");
                skupna_cena_izpis.setText("");
            }
            if ((lV == lP && mV-mP > 1) || (lV == lP && mV-mP == 1 && dV > dP) || (lV > lP && mV > 1)) {
                status.setForeground(Color.red);
                status.setText("Status: Največji možen čas izposoje je en mesec.");
                pravilno = false;
                cena_na_dan_izpis.setText("");
                skupna_cena_izpis.setText("");
            }

            if (pravilno) {
                int stDni = 0;
                if (mV == mP) {
                    stDni = dV - dP +1;
                }
                else {
                    if (mP == 2) {
                        stDni = 28 - dP + dV +1;
                    }
                    else if (mP == 4 || mP == 6 || mP == 9 || mP == 11) {
                        stDni = 30 - dP + dV +1;
                    }
                    else {
                        stDni = 31 - dP + dV +1;
                    }
                }

                int cena = 0;
                if (majhen.isSelected())  cena = 70;
                else if (srednji.isSelected())  cena = 100;
                else if (velik.isSelected())  cena = 150;
                if (avtomatski.isSelected())  cena += 10;
                if (dizelski.isSelected())  cena += 15;
                cena_na_dan_izpis.setText(cena + " €");

                int skupnaCena = cena * stDni; 
                if (dod_zavarovanje.isSelected()) skupnaCena += 2*stDni;

                skupna_cena_izpis.setText(skupnaCena +" €");
                
                if (!placano) {
                    status.setForeground(Color.black);
                    status.setText("Status: Skupna cena trenutno izbranega avtomobila za izbrano obdobje je " + skupnaCena + " €.");
                }
            }
        }
    }//GEN-LAST:event_formMouseEntered

