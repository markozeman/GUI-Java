    private void IzvediActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IzvediActionPerformed
        // TODO add your handling code here:
        String vnos = PoljeZaVnos.getText();

        if (Dodaj.isSelected()) {
            boolean jeNotri = false;
            int a = jComboBox1.getItemCount();
            String[] s = new String [a];
            for (int i=0; i<a; i++) {
                s[i] = jComboBox1.getItemAt(i).toString();
                if (s[i].equals(vnos)) {
                    jeNotri = true;
                }
            }
            if ((!vnos.isEmpty() && !jeNotri) || (!vnos.isEmpty() && jeNotri && !PrepovejDvojnike.isSelected())) {
                jComboBox1.addItem(vnos);
            }
            VrsticaZaSporocila.setText("Vrstica za sporočila: Dodajam");
        }
        else if (OdstraniPrvega.isSelected()) {
            if (jComboBox1.getItemCount() != 0) {
                jComboBox1.removeItemAt(0);
            }
            VrsticaZaSporocila.setText("Vrstica za sporočila: Odstranjujem prvega");
        }
        else if (OdstraniIzbranega.isSelected()) {
            if (jComboBox1.getItemCount() != 0) {
                int i = jComboBox1.getSelectedIndex();
                jComboBox1.removeItemAt(i);
            }
            VrsticaZaSporocila.setText("Vrstica za sporočila: Odstranjujem izbranega");
        }

    }//GEN-LAST:event_IzvediActionPerformed

    
