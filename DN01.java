    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        // TODO add your handling code here:
        int a = (int) jSpinner1.getValue();
        int count = jComboBox1.getItemCount();
        if (a>=count) {
            VrsticaZaSporocila.setText("Vrstica za sporočila: Ni elementa");
        }
        else {
            String s = jComboBox1.getItemAt(a).toString();
            VrsticaZaSporocila.setText("Vrstica za sporočila: "+s);
        }
    }//GEN-LAST:event_jSpinner1StateChanged

