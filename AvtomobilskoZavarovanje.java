    private void preveri_datumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_preveri_datumActionPerformed
        // TODO add your handling code here:
        int d_index = dan.getSelectedIndex();
        int d = d_index+1;
        int m_index = mesec.getSelectedIndex();
        int m = m_index+1;
        int l_index = leto.getSelectedIndex();
        int l = l_index+1930;
        
        boolean pravilno = true;
        boolean prestopno = false;
        
        if ((l%4 == 0 && l%100 != 0) || (l%4 == 0 && l%100 != 0 && l%400 == 0)) {
            prestopno = true;
        }
        
        if (d == 31 && (m == 4 || m == 6 || m == 9 || m == 11)) {            
            pravilno = false;
        }
        
        if (m == 2 && d > 29) {
            pravilno = false;
        }
        
        if (m == 2 && d == 29 && !prestopno) {
            pravilno = false;
        }
        
        if (!pravilno) {
            Status.setForeground(Color.red);
            Status.setText("Status: Izbran datum ni veljaven.");
            preveri_datum.setBackground(new Color(255,69,7));
        }
        else {
            Status.setForeground(Color.black);
            Status.setText("Status: Izbran datum je veljaven.");
            preveri_datum.setBackground(Color.green);
        }
    }//GEN-LAST:event_preveri_datumActionPerformed

