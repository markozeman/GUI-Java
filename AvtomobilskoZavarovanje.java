    private void shrani_vnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shrani_vnosActionPerformed
        // TODO add your handling code here:
        boolean prazno = false;
        
        if (ime_vnos.getText().equals("") || priimek_vnos.getText().equals("") || ulica_vnos.getText().equals("") || hisna_st_vnos.getText().equals("") ||
                kraj_vnos.getText().equals("") || postna_st_vnos.getText().equals("") || znamka_vnos.getText().equals("") ||
                model_vnos.getText().equals("") || poraba_goriva_vnos.getText().equals("") || moc_motorja_vnos.getText().equals("") ||
                prostornina_motorja_vnos.getText().equals("") || barva_vnos.getText().equals("") || stevilo_sedezev_vnos.getText().equals("") ||
                kraj_registracije_vnos.getText().equals("") || registrska_tablica_vnos.getText().equals("")) 
        {
            Status.setForeground(Color.red);
            Status.setText("Status: Izpolnite vsa polja!");
            prazno = true;
        }
        
        if(!prazno) {
            
            try {
                
                File f = new File ("/home/student/FRI/KČR/DN03/Izpolnjen_obrazec.txt");
                if (!f.exists()) {
                    f.createNewFile();
                }    
                
                FileWriter fw = new FileWriter(f.getAbsoluteFile());
                BufferedWriter bw = new BufferedWriter(fw);
                
                bw.write("PODATKI O ZAVAROVANCU \n");
                bw.write("Ime: " + ime_vnos.getText() + "\n");
                bw.write("Priimek: " + priimek_vnos.getText() +"\n");
                bw.write("Kraj: " + kraj_vnos.getText() +"\n");
                bw.write("Poštna številka: " + postna_st_vnos.getText() +"\n");
                bw.write("Ulica: " + ulica_vnos.getText() +"\n");
                bw.write("Hišna številka: " + hisna_st_vnos.getText() +"\n");
                int d = dan.getSelectedIndex() + 1;
                int m = mesec.getSelectedIndex() + 1;
                int l = leto.getSelectedIndex() + 1930;
                bw.write("Datum rojstva: " + d + ". " + m + ". " + l + "\n");
                if (mlad.isSelected())  bw.write("Izkušnje: " + "mlad voznik" + "\n\n");
                else  bw.write("Izkušnje: " + "izkušen voznik" + "\n\n");
                
                bw.write("PODATKI O VOZILU\n");
                bw.write("Znamka: " + znamka_vnos.getText() + "\n");
                bw.write("Model: " + model_vnos.getText() + "\n");
                bw.write("Moč motorja: " + moc_motorja_vnos.getText() + " kW\n");
                bw.write("Prostornina motorja: " + prostornina_motorja_vnos.getText() + "cm3\n");
                bw.write("Poraba goriva: " + poraba_goriva_vnos.getText() + "L/100km\n");
                bw.write("Barva: " + barva_vnos.getText() + "\n");
                bw.write("Število sedežev: " + stevilo_sedezev_vnos.getText() + "\n");
                if (osebni_avto.isSelected())  bw.write("Vrsta vozila: osebni avto\n\n");
                else if (motor.isSelected())  bw.write("Vrsta vozila: motor\n\n");
                else if (tovornjak.isSelected())  bw.write("Vrsta vozila: tovornjak\n\n");
                else if (traktor.isSelected())  bw.write("Vrsta vozila: traktor\n\n");
                else if (avtobus.isSelected())  bw.write("Vrsta vozila: avtobus\n\n");
                
                bw.write("PODATKI O REGISTRACIJI\n");
                bw.write("Kraj prve registracije: " + kraj_registracije_vnos.getText() + "\n");
                int d1 = dan1.getSelectedIndex() + 1;
                int m1 = mesec1.getSelectedIndex() + 1;
                int l1 = leto1.getSelectedIndex() + 1980;
                bw.write("Datum prva registracije: " + d1 + ". " + m1 + ". " + l1 + "\n");
                bw.write("Registrska tablica: " + registrska_tablica_vnos.getText() +"\n\n");
                
                bw.write("PODATKI O ZAVAROVANJU\n");
                if (AO.isSelected())  bw.write("Osnovno zavarovanje: AO\n");
                else if (AO_plus.isSelected())  bw.write("Osnovno zavarovanje: AO+\n");
                if (brez.isSelected())  bw.write("Kasko zavarovanje: brez\n");
                else if (odbitna_fransiza.isSelected())  bw.write("Kasko zavarovanje: odbitna franšiza\n");
                else if (polno.isSelected())  bw.write("Kasko zavarovanje: polno\n");
                bw.write("Dodatna zavarovanja: ");
                if (stekla.isSelected())  bw.write("stekla, ");
                if (gume.isSelected())  bw.write("gume, ");
                if (na_parkiriscu.isSelected())  bw.write("na parkirišču, ");
                if (proti_kraji.isSelected())  bw.write("proti kraji, ");
                if (proti_toci.isSelected())  bw.write("proti toči, ");
                if (tretja_oseba.isSelected())  bw.write("potniki, ");
                if (potniki.isSelected())  bw.write("potniki");
                
                
                bw.close();
                
                Status.setForeground(new Color (0,200,0));
                Status.setText("Status: Podatki uspešno shranjeni.");
                
            }
            catch (IOException e) {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_shrani_vnosActionPerformed

